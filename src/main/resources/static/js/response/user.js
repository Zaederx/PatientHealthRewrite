"use strict";
class User {
    constructor(id, name, username, email, role, messagesSent, messagesRecieved) {
        this.id = id;
        this.name = name;
        this.username = username;
        this.email = email;
        this.role = role;
        this.messagesSent = messagesSent;
        this.messagesRecieved = messagesRecieved;
    }
}
