"use strict";
class Week {
    constructor(weekNumber, mondayDateStr, monday, tuesday, wednesday, thursday, friday, saturday, sunday) {
        this.weekNumber = weekNumber;
        this.mondayDateStr = mondayDateStr;
        this.monday = monday;
        this.tuesday = tuesday;
        this.wednesday = wednesday;
        this.thursday = thursday;
        this.friday = friday;
        this.saturday = saturday;
        this.sunday = sunday;
    }
}
